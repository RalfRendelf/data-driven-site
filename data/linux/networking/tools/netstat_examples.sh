# Просмотр всех активных соединений
netstat -a

# Просмотр активных TCP-соединений
netstat -at

# Просмотр активных UDP-соединений
netstat -au

# Просмотр активных соединений с отображением PID процессов
netstat -ap

# Просмотр статистики по протоколам
netstat -s

# Просмотр таблицы маршрутизации
netstat -r

# Просмотр статистики сетевых интерфейсов
netstat -i
