# Проверка доступности хоста
ping google.com

# Проверка доступности с указанием количества пакетов
ping -c 4 google.com

# Проверка доступности с интервалом между пакетами
ping -i 2 google.com

# Проверка доступности с указанием размера пакета
ping -s 1000 google.com

# Проверка доступности в фоне
ping -f google.com

# Проверка доступности с таймаутом
ping -W 3 google.com
